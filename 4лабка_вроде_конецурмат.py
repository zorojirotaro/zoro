for metric in ['stress','satisfaction','sleep_hours','sport']:
    if metric in df.columns:
        t_stat, p_val = stats.ttest_ind(male[metric], female[metric])
        diff = male[metric].mean() - female[metric].mean()
        if p_val < 0.05:
            sign = "✓" if diff>0 else "✓"
            print(f"  {sign} {metric}: {'Мужчины выше' if diff>0 else 'Женщины выше'} на {abs(diff):.2f} (p={p_val:.3f})")
        else:
            print(f"    {metric}: нет значимых различий (p={p_val:.3f})")

