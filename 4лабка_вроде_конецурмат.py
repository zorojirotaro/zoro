axes[0,0].set_ylabel(f'PC2 ({pca.explained_variance_ratio_[1]:.1%})')
plt.colorbar(scatter, ax=axes[0,0])

# K-Means на t-SNE
scatter = axes[0,1].scatter(X_tsne[:,0], X_tsne[:,1], c=df['cluster_kmeans'], cmap='viridis', s=100, alpha=0.7)
axes[0,1].set_title(f'K-Means (k={best_k}) - t-SNE', fontsize=12)
axes[0,1].set_xlabel('t-SNE 1')
axes[0,1].set_ylabel('t-SNE 2')
plt.colorbar(scatter, ax=axes[0,1])

