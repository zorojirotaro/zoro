# Рассчитываем корреляцию
correlations = {}
for col in corr_cols:
    correlations[col] = df[col].corr(df['stress'])

# Сортируем
corr_series = pd.Series(correlations).sort_values()

print("\n  🔽 СНИЖАЮТ стресс (отрицательная корреляция):")
negative_corr = corr_series[corr_series < 0]
for i, (var, corr_val) in enumerate(negative_corr.head(3).items()):
    print(f"    {i+1}. {var}: {corr_val:.3f}")

print("\n  🔼 ПОВЫШАЮТ стресс (положительная корреляция):")
positive_corr = corr_series[corr_series > 0]
for i, (var, corr_val) in enumerate(positive_corr.tail(3).iloc[::-1].items()):
    print(f"    {i+1}. {var}: {corr_val:.3f}")

# ============= 3. ГЕНДЕРНЫЕ РАЗЛИЧИЯ =============
