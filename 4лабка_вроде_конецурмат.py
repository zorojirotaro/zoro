print(f"✅ Используем признаки: {available_features}")

X = StandardScaler().fit_transform(df[available_features])

# PCA и t-SNE для визуализации
pca = PCA(n_components=2, random_state=42)
X_pca = pca.fit_transform(X)
print(f"✅ PCA объясняет {pca.explained_variance_ratio_.sum():.1%} дисперсии")

perplexity = min(20, n-1)
tsne = TSNE(n_components=2, random_state=42, perplexity=perplexity)
X_tsne = tsne.fit_transform(X)
print(f"✅ t-SNE выполнено (perplexity={perplexity})")

# K-MEANS с подбором k
print("\n📊 K-MEANS:")
scores = []
k_range = range(2, min(6, n))
for k in k_range:
    labels = KMeans(n_clusters=k, random_state=42, n_init=10).fit_predict(X)
    score = silhouette_score(X, labels)
    scores.append(score)
    print(f"  k={k}: silhouette={score:.3f}")

best_k = k_range[np.argmax(scores)]
print(f"\n✨ Оптимальное k = {best_k}")

kmeans = KMeans(n_clusters=best_k, random_state=42, n_init=10)
df['cluster_kmeans'] = kmeans.fit_predict(X)

# ИЕРАРХИЧЕСКАЯ
print("\n📊 ИЕРАРХИЧЕСКАЯ:")
hier = AgglomerativeClustering(n_clusters=best_k, metric='euclidean', linkage='ward')
df['cluster_hier'] = hier.fit_predict(X)

# Дендрограмма
plt.figure(figsize=(12, 5))
linked = linkage(X, method='ward')
dendrogram(linked, truncate_mode='lastp', p=30, leaf_rotation=90, leaf_font_size=8)
plt.title('Дендрограмма (иерархическая кластеризация)', fontsize=12)
plt.xlabel('Индексы студентов')
plt.ylabel('Расстояние')
plt.axhline(y=5, color='r', linestyle='--', alpha=0.7)
plt.tight_layout()
plt.show()

# DBSCAN
print("\n📊 DBSCAN:")
best_db = None
best_score = -1
for eps in [0.8, 1.0, 1.2, 1.5]:
    for min_samples in [2, 3, 4]:
        labels = DBSCAN(eps=eps, min_samples=min_samples).fit_predict(X)
        n_clust = len(set(labels)) - (1 if -1 in labels else 0)
        n_noise = list(labels).count(-1)
        if 2 <= n_clust <= 5:
            mask = labels != -1
            if sum(mask) > 1:
                try:
                    score = silhouette_score(X[mask], labels[mask])
                    print(f"  eps={eps}, min_samples={min_samples}: кластеров={n_clust}, шум={n_noise}, score={score:.3f}")
                    if score > best_score:
                        best_score, best_db = score, (eps, min_samples, labels)
                except:
                    print(f"  eps={eps}, min_samples={min_samples}: кластеров={n_clust}, шум={n_noise}")

if best_db:
    eps, ms, labels = best_db
    df['cluster_dbscan'] = labels
    print(f"\n✨ Лучший DBSCAN: eps={eps}, min_samples={ms}, выбросов={list(labels).count(-1)}")

# Визуализация кластеров
