axes[1,0].set_title(f'Иерархическая (k={best_k}) - PCA', fontsize=12)
axes[1,0].set_xlabel(f'PC1 ({pca.explained_variance_ratio_[0]:.1%})')
axes[1,0].set_ylabel(f'PC2 ({pca.explained_variance_ratio_[1]:.1%})')
plt.colorbar(scatter, ax=axes[1,0])

# DBSCAN на PCA (если есть)
if best_db:
    scatter = axes[1,1].scatter(X_pca[:,0], X_pca[:,1], c=df['cluster_dbscan'], cmap='tab10', s=100, alpha=0.7)
    axes[1,1].set_title(f'DBSCAN - PCA (выбросов: {list(df["cluster_dbscan"]).count(-1)})', fontsize=12)
    axes[1,1].set_xlabel(f'PC1 ({pca.explained_variance_ratio_[0]:.1%})')
    axes[1,1].set_ylabel(f'PC2 ({pca.explained_variance_ratio_[1]:.1%})')
    plt.colorbar(scatter, ax=axes[1,1])
else:
    axes[1,1].text(0.5, 0.5, 'DBSCAN не дал\nоптимальных результатов',
                   ha='center', va='center', transform=axes[1,1].transAxes, fontsize=12)
    axes[1,1].set_title('DBSCAN', fontsize=12)

plt.tight_layout()
plt.show()

